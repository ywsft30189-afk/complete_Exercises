# count from 0 to 50 by 1
print("-" * 26)
print("Counting up from 0 to 50:")
print("-" * 26)
for i in range(0, 51):
    print(i)
print("------------------------------")
# count from 50 down to 0 by 1
print("-" * 26)
print("counting down from 50 to 0:")
print("-----------------------------")
for i in range(50, -1, -1):
    print(i)
print("------------------------------")

